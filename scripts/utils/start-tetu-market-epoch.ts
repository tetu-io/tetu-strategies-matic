import {ethers} from "hardhat";


const TETUBAL_BRIBE_VAULT_ADDRESS = '0x2dE7ab57966f7C98be4252f16350e7B185680020'
const proposalId = '0xb3e5a3a5993f786e34d030c32af0f855701740ea6f322f9b624a327487bc4c1a';
const roundNumber = '23';
const deadline = '1687824000';

async function main() {
  const [signer] = await ethers.getSigners();

  const contract = new ethers.Contract(
    TETUBAL_BRIBE_VAULT_ADDRESS,
    VAULT_ABI,
    ethers.provider
  )

  const tx = await contract.connect(signer).createEpoch(
    ethers.utils.keccak256(proposalId),
    ethers.BigNumber.from(roundNumber),
    ethers.BigNumber.from(deadline),
  )

  console.log(`https://polygonscan.com/tx/${tx.hash}`)
  await tx.wait()
  console.log('tx mined')

}

main()
  .then(() => process.exit(0))
  .catch(error => {
    console.error(error);
    process.exit(1);
  });


